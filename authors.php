<h4 id="mt">Manuscript Titles </h4>
<p>Manuscript titles should be written in sentence case (only the first letter of the first word of the title, proper nouns, and other genus names should be capitalized). Authors should avoid the use of specialist abbreviations in the title if possible. For clinical trials, systematic reviews (with or without meta-analyses), the subtitle should indicate the respective study design.</p>

<h4>Author Names and Affiliations</h4>
<p>All authors included in any submission must meet the criteria for authorship as specified in the international authorship policy recommended by <a href="https://www.icmje.org/recommendations/browse/roles-and-responsibilities/defining-the-role-of-authors-and-contributors.html#two" target="_blank" style="color: blueviolet;">ICMJE here</a>. All authors should have made substantial contributions to the submitted work and should be accountable for the work. According to ICMJE recommendations, authors must meet all four criteria as given below: <br>
    <ul style="margin-top: -30px;">
        <li>Substantial contribution to conception and design, acquisition of data, or analysis and interpretation of data.</li>
        <li>Drafting the article or revising it critically for important intellectual content.</li>
        <li>Final approval of the version to be published.</li>
        <li style="margin-bottom: 10px;">Agreement to be accountable for all aspects of the work in ensuring that questions related to the accuracy or integrity of any part of the work are appropriately investigated and resolved.</li> 

        <p style="margin-bottom: -6px;">On the title page, authors’ names should be written in the following order:</p>
        <li>First name</li>
        <li>Middle name (or initials, if used)</li>
        <li style="margin-bottom: -20px;">Last name (surname, family name)</li>
    </ul> 
    Names of author will be published exactly as they appear in the manuscript file. All authors should therefore ensure that names given in the manuscript files are correct. <br> <span id="ca"></span> Anyone who contributed to the submitted work but do not meet the above criteria for authorship should be mentioned in the Acknowledgments. 

All authors included in a submission must have an affiliation. Usually, the affiliation includes department, university, or organizational affiliation and its location, including city, and country.  Authors with multiple affiliations should include them on the manuscript title page, but only the preferred or primary affiliation should be included in the manuscript submission system. 

</p>

<h4>Corresponding Author</h4>
<p>
The author submitting the manuscript will be designated as the corresponding author in ASFIRJ submission system. <span id="cta"></span>The corresponding author will be the primary point of contact with ASFIRJ in all matters related to the submitted manuscript. He or she will be the only one among the manuscript authors who will be able to make any required changes to the manuscript during the review process. The corresponding author may wish to collate and include the ORCID ID of the authors in the manuscript file and the submission system.</p>

<h4>Changes to Authorship </h4>
<p>Prior to submitting their manuscripts, authors should carefully check the authorship list of their manuscript and provide the complete list of authors at the time of the original submission. Any changes to the authorship list, including addition, deletion, and changes in the order of names in the authorship list must be made before the manuscript is accepted and these changes must be approved by the journal editors. If authors would like to make any of these changes, the corresponding author should send an official request in that regard to the editor. <br> 
    
    The letter should include the following items: (a) the reason for the change in author list, <span id="cl"></span> and (b) written confirmation from all authors agreeing to the change. <br>  If an author is removed or added, the removed and added authors must confirm the change.  Except in exceptional circumstances, the editor will not consider any changes to the authors list after the manuscript has been accepted. In such exceptional circumstances, publication of the accepted paper will be put on hold until the editor makes a decision on the change. If the manuscript has already been published online, any requests for change in the authorship list will result in an erratum if approved by the editor.</p>

<h4>Cover letter</h4>
<p style="margin-bottom: -6px;">All submissions to <i>ASFIRJ</i> must be accompanied by a cover letter. Authors should note that the cover letter is usually the first part of the submitted manuscript that the editor reads and thus gives the first impression of the authors and their work. The cover letter must therefore be written with great attention. Authors can adapt the following structure in writing their cover letter:</p> <br>

<b>First paragraph: </b>
<ul style="margin-bottom: -6px;">
    <li>The title of the manuscript should be the title of the cover letter</li>
    <li>Give the rationale for the study and highlight the question the manuscript sought to answer</li>
</ul>
<b>Second paragraph:</b> <br>
<ul style="margin-bottom: -6px;">
    <li>Briefly present what was done in the study and how it was done</li>
    <li>Present the key results and why they are important</li>
</ul>
<b>Third paragraph:</b> <br>
<ul style="margin-bottom: -6px;">
    <li>Persuade the reasons why the readers of ASFIRJ should be interested in the work</li>
    <li>Take your cues from the journal’s aims and scope</li>
</ul>
<b>Fourth paragraph:</b><span id="cot"></span> <br>
All cover letters of works submitted to ASFIRJ should contain the following sentences:
<ul>
    <li>We confirm that this manuscript is original, has not been published elsewhere, and is not under consideration by another journal while being submitted to ASFIRJ. </li>
    <li>All authors report no conflict of interest to this submission, have read and approved the manuscript and agree with its submission to ASFIRJ.</li>
</ul>

<h4>Conflicts of Interest </h4>
<p>All authors must disclose all financial relationships that involved the submitted manuscript, including all sources of funding used to undertake their work,<span id="ref"></span> any commercial involvement that constitute a conflict of interest, any consultations, speakers' participation, stock or other equity ownership, patent arrangements, or employment. A statement of conflict of interest must be clearly stated in the manuscript file, after the Acknowledgments. It is the responsibility of the corresponding author collate each author’s statement and all authors should approve the complete disclosure before submission to ASFIRJ.
</p>

<h4>References</h4>
<p>Authors are required to do due diligence to cite relevant and update-to-date references in their work. 
    <br>All available references on a topic can be cited in the reference list and can include the following: 
    
    <br>(1) published articles or manuscripts accepted for publication; <br>(2) papers on preprint servers that have citable DOI or preprint server’s URL. 
    
    <br> <br> The following sources are not accepted as references: <br> 
    (1) unpublished work, including manuscripts not yet accepted in a journal; <br>
    (2) personal communications; <br>
    (3) retracted works. <br id="tab">

     ASFIRJ follows the Vancouver reference style. The reference list must include full bibliographic details for all works cited in the work. References should be listed at the end of the manuscript and numbered in the order that they appear in the text, listing the first six authors, et al. In the text, the reference number should be cited as a superscript. Authors should not include citations in the abstracts. 
   </p> 

<h4>Tables</h4>
<p> 

Tables should be placed at the end of the manuscript, after the reference list. <span id="fig"></span>Table titles should appear above each table with clear and descriptive information about the content of the table, usually starting with a label such as “Table 1”. In the manuscript text, tables should be cited in ascending numeric order upon first appearance (e.g., “Table 1”) immediately following the sentence or paragraph where the table is first cited. Tables can also be submitted as separate files if the authors wish to do so. Authors should place any explanatory or footnotes, and other text below the tables.</p> 

<h4>Figure Legends</h4>
<p>Any figure included in the manuscript should be placed at the end of the manuscript, immediately after the tables or reference list if tables are not included in the manuscript. Figure legends should appear below each figure with clear and descriptive information about the content of the figure. In the manuscript text, figures should be cited in ascending numeric order upon first appearance (e.g., “Figure 1”) immediately following the sentence or paragraph where the figure is first cited. Figures can also be submitted as separate files if the authors wish to do so. Any explanatory or footnotes should be placed after the figure legend.
 
</p>



