<h4>Original Articles:</h4> 
<p>Original articles report new research findings or conceptual analyses that contribute significantly to a specific area of knowledge. These reports may include intervention or experimental studies (such as randomized trials), cohort studies, case-control studies, cross-sectional studies, surveys, cost-effectiveness analyses and decision analyses, studies of screening and diagnostic tests, and other observational studies. ASFIRJ welcomes high quality original articles from any field of research and discipline. Original submissions should comply with the following structure.
 <br> <br>
    <b>Title:</b>The title should be informative with less than 200 characters. <br>
 

<div><b>ABSTRACT (structured abstract)</b></div>
<div><b>Background:</b> Rationale for the study</div>
<div><b>Objective:</b> Aim of the study</div>
<p><b>Methods:</b></p>	
<ul style="margin-top: -30px; margin-bottom: 10px;">
    <li>Design: Type of study</li>
    <li>Recruitment and data collection, including the inclusion and exclusion criteria (specifying aspects considered, e.g., study design, participants, interventions and outcomes, where appropriate) and primary data collection tool/approach employed.</li>
    <li>Main data analysis approaches and strategies</li>
</ul>

<div><b>Results:</b> Main findings</div>
<div><b>Conclusions:</b> Main conclusions and their implications</div>
<div><b>Maximum length:</b> 300 words</div> <br>

<b style="margin-top: 10px;">MAIN TEXT</b> <br>
<div><b>Introduction:</b> What is the context, rationale, and aims of the study</div>
<div><b>Methods:</b> What was done in the study, including all study procedures used, recruitment of study participants, methods of data collection, materials used in the study, statistical analyses performed, and issues of ethics.</div>
<div><b>Results:</b> What the study found, including descriptive results and main findings of the study </div> 
<div><b>Discussion: </b>Summary of key findings, strengths and limitations of the study, comparison with previous studies on the topic, interpretation of the findings, implications of findings, conclusions</div>	
<div><b>Maximum length: </b>4,500 words (excluding abstract, tables, figures, acknowledgments, references)</div>

<div><b>Keywords:</b> up to 10, listed in alphabetical order. <span id="ct"></span></div>
<div><b>Tables/Figures:</b> up to 10 tables and/or figures </div> <br>


<h4>Clinical Trials:</h4> 
<p>A clinical trial is any research study in which participants are prospectively assigned
    to one or more related interventions to evaluate the effects of the said interventions
    on pre-specified outcomes. <br> <br>
    Clinical trials should follow the <a href="https://www.icmje.org/" target="_blank" style="color: blueviolet;">International Committee of Medical Journal Editors
        (ICMJE)</a> requirements. Clinical trials must be registered in a WHO-approved public
trials registry before or at the onset of enrollment of study participants to be
considered for publication. Authors should include the trial registry name, URL, and
registration number at the end of the abstract of their manuscript. <br> <br>

Full details of clinical trial registration and the necessary requirements can be found
on the <b>International Committee of Medical Journal Editors (ICJME)</b> website. <br>
Please note that the aims given in the manuscript should be consistent with the aims
in the clinical trial registry. <br> <br>
Reporting clinical trials should follow the relevant <a href="https://www.equator-network.org/" target="_blank" style="color: blueviolet;">EQUATOR Network guidelines</a>. All
randomized controlled trials should include a completed <a href="https://journals.sagepub.com/pb-assets/cmscontent/Z_DO%20NOT%20DELETE/consort-2010-flow-diagram-1709161190537.pdf" target="_blank" style="color: blueviolet;">CONSORT flow chart.</a> The
<a href="https://www.equator-network.org/wp-content/uploads/2017/05/CONSORT-2010-Checklist.doc" target="_blank" style="color: blueviolet;">CONSORT checklist</a> should be uploaded with submission as a supplementary file
and <a href="https://www.cdc.gov/trendstatement/" target="_blank" style="color: blueviolet;">TREND</a> for non-randomized trials, and other specialized guidelines as
appropriate. <br> <span id="e"></span>
The methodology section should contain details about randomization, patient and
control assignments, and intervention. Primary and secondary outcome measures
should be included in detail.
    </p>



<h4>Editorials </h4>
<p><i>ASFIRJ</i> will publish editorials by invitation only. Editorials are commissioned by the Editorial board and after consultation with the Editors-in-Chief.<br>
    <b>Title:</b> Should be informative that establishes a link to the article that the Editorial is written for. <br>
    
    <b>Text:</b> Should be strictly limited to 1,000 words. There will not be any abstracts. <br>
    <b>References:</b> Maximum 9 references are allowed. <br>
    <b>Figures and tables:</b> <span id="rl"></span> Maximum two display items, figures and/or tables. Figure legends should be concise and should not be more than 100 words.</p>

<h4>Research Letters:</h4>
<p>Research letters constitute novel or unexpected finding or observation or importance in a specific field and thus may have an immediate impact on the field. Research letters are considered as original papers, but are not extensive by nature to the extent of full original papers. Having said that, the review process of research letters is as rigorous as that of original paper. Research letters usually have no abstract and should begin with <b>‘To the Editor’</b>. If an original paper submission is not considered significant enough to merit publication as a full article, <i>ASFIRJ</i> may ask authors to reformat it as a research letter. Supplementary information can accompany research letters. <br>

    <b>Word count:</b> 1,500 words <span id="c"></span> <br>
    <b>Figures and tables:</b> Maximum of 3 figures and/or tables <br>
    <b>References:</b> Maximum of 10 references</p>


<h4>Correspondences:</h4> 
<p>Correspondences are letters written concerning recent publication in the journal. <i>ASFIRJ</i> encourages such correspondences as this enhances interactions between article authors and readers and other experts in the field. Correspondences must be received within one month of publication of the original paper. If the editor considers the correspondence acceptable, the authors of the original paper will be invited to respond, and the response will be published alongside the correspondence. <i>ASFIRJ</i> accepts two types of correspondence letters: (1) correspondences that concern recent papers published in <i>ASFIRJ</i>, and (2) correspondence that concern recent papers published in other journals other than <i>ASFIRJ</i>. <br>

    <b>Word count:</b> Not than 500 words. <br>
    <b>Title:</b> Have a short, relevant title that is different from the title of the original paper being discussed <br>
    <b>Title page:</b> Have a complete title page <br>
    <b>References:</b> Listed at the end of the letter with the journal article being discussed as the first reference  <br>
    <b>Graphics:</b> <span id="rsr"></span> Have no more than one graphic presentation (table or figure) <br>
    <b>Begin with:</b> "To the Editor:"
</p>



<h4 >Systematic Reviews and Meta-Analysis:</h4>
<p><i>ASFIRJ</i> encourages the submission of systematic review and meta-analysis papers. Systematic reviews can be accompanied with or without meta-analyses, which is determined by the nature and homogeneity of quantitative estimates collected during the systematic review process. Systematic reviews (with or without meta-analyses) require a comprehensive systematic search of the literature undertaken in multiple databases, usually covering many years, and appraisal of the quality of included studies. Systematic reviews should address a specific question or issue that is relevant for practice or policy in relevant topic and should provide an evidence-based, balanced, and high quality review on a focused topic. Systematic reviews being submitted in <i>ASFIRJ</i> should be registered in appropriate platforms, e.g., PROSPERO or Open Science Framework, normally before the research is conducted. Systematic reviews are published in <i>ASFIRJ</i> as original studies. All systematic review submissions should be accompanied by the relevant checklist, e.g., PRISMA, and follow <a href="" style="color: blueviolet;">EQUATOR Reporting Guidelines</a>. <br>

    <b>Title:</b> <br>
    The title should be informative with less than 120 characters. Titles should include “systematic review” if no meta-analysis was performed, but “systematic review and meta-analysis” if a systematic review was performed. <br> <br>

    <b>ABSTRACT </b><br>
    <b>Background:</b> Rationale for the review <br>
    <b>Objective:</b> Aim of the review <br>
    <b>Methods:</b>
    <ul style="margin-top: -30px; margin-bottom: -6px;">
        <li>Design: Type of systematic review or meta-analysis, and methods for study appraisal (risk of bias assessment)</li>
        <li>Data sources and study selection: which databases were searched to identify studies</li>
        <li>Eligibility criteria for selecting studies: inclusion and exclusion criteria (specifying aspects considered, e.g., study design, participants, interventions and outcomes, where appropriate).</li>
    </ul>
    <b>Results:</b> Main findings <br>
    <b>Conclusions:</b> Main conclusions and their implications <br>
    <b>Systematic review registration:</b> registry and number <br>
    <b>Maximum length:</b> 300 words <br> <br>

    <b>MAIN TEXT</b> <br>
    <b>Introduction:</b> Rationale, Objective(s) <br>
    <b>Methods:</b> Eligibility criteria, information sources, search strategy, selection process, data items, risk of bias, assessment, synthesis methods <br>
    <b>Results:</b> Study selection, study characteristics, risk of bias in studies, results of individual studies, results of syntheses <br>
    <b>Discussion:</b> <span id="re"></span> Summary of key findings, strengths and limitations of the systematic review, comparison with previous studies on the topic, interpretation of the findings, implications of findings, conclusions <br>
    <b>Maximum length:</b> 4,500 words (excluding abstract, tables, figures, acknowledgments, references) <br>
</p>



<h4>Review Articles: </h4>
<p>
    Review articles present an update of the most recent developments in a particular field. They differ by the scope and level of analysis of the literature searches and the titles used from the systematic reviews. Although they do not usually require a rigorous literature search, they should rely on evidence and should be written by established experts in the field. <br>

<b>Abstract:</b> 300 words (unstructured summary) <br> <br>
<b>MAIN TEXT:</b> <br>
Less than 4,500 words not including abstract, figure legends and references (please supply a word count). <br>
Additional unlimited online supporting information can be provided. <br>
Up to 200 references in the Journal's style. <span id="p"></span> <br>
Figures and tables are important in review papers and up to 10 figures and/or tables (total) can be included in the text.

</p>

<h4>Protocols</h4>
<p>Protocols are papers that report planned or ongoing research studies. Protocols represent a road-map document for a piece of research study and outline the approaches authors will employ in addressing their research questions. It is nowadays common to publish study protocols prior to undertaking the research, in addition to registering them with relevant registration repositories. <i>ASFIRJ</i> encourages the submission of study protocols across all study types. <br>

   An important benefit of submitting a protocol, particularly at an early stage of the study, is that it provides the authors an opportunity to get feedback from peers in the topic area, which can be valuable in improving their work. Additionally, published protocols provide researchers and funders an opportunity to stay abreast of studies in their field of interest. Published protocols can also help to prevent unwarranted duplication of studies, facilitate collaborations, increase transparency, help others to appreciate any deviations between the protocol and the completed study. Protocols submitted when data collection for the study is completed will not be considered in <i>ASFIRJ</i>. <br>
    
    The EQUATOR Network <a href="https://www.equator-network.org/" style="color: blueviolet;">(https://www.equator-network.org/)</a>  provides guidelines for structuring and reporting protocols of different study designs in the biomedical fields, which can also be adapted to other scientific fields. <i>ASFIRJ</i> encourages authors to follow these recommendations when drafting the protocols for their studies. We recommend that the protocol manuscripts should contain the following items: <br>

    <b>ABSTRACT (structured abstract)</b> <br>
    <b>Background:</b> Rationale for the planned or ongoing study <br>
<b>Objective:</b> Aim of the study <br>
<b>Methods:</b> <br>
Design: Type of study Recruitment and data collection, including the inclusion and exclusion criteria (specifying aspects considered, e.g., study design, participants, interventions and outcomes, where appropriate) and primary data collection tool/approach employed.
Main data analysis approaches and strategies <br>
<b>Conclusions:</b> Main conclusions and their implications <br>
<b>Maximum length:</b> 200 words. <br> <br>

<b>MAIN TEXT</b> <br>
<b>Introduction:</b> What is the context, rationale, and aims of the study. <br>
<b>Methods:</b> What will be done in the study, including all study procedures used, recruitment of study participants, methods of data collection, materials used in the study, statistical analyses performed, and issues of ethics. <span id="persp"></span> <br>
<b>Discussion:</b> Potential implications of study when completed. <br>
<b>Maximum length:</b> 2,000 words (excluding abstract, tables, figures, acknowledgments, references) <br>
<b>Keywords:</b> up to 10, listed in alphabetical order. <br>
<b>Tables/Figures:</b> up to 5 tables and/or figures.


    </p>

<h4>Perspectives:</h4>
<p>The <i>ASFIRJ</i> ‘Perspective’ papers are article types that describe the author’s viewpoints in an area of scientific or research interest, from the lens of current developments, previous works, and personal experiences. They allow authors to share their perspectives and insights on various issues on a topic area, particularly within the research context in Africa. It is a place for authors to tell their story and share their perspectives for the benefits of colleagues and the larger scientific community. The content of this article type should include discussion of the prevailing (current) issues on the topic area; the advances and progress being made on the topic area; and a persuasive and clear description and presentation of the author’s perspectives. Authors should include relevant references of previous works in the topic area. Authors can also include their own data to support their viewpoints. While the structure of ‘Perspective’ papers are similar to the Narrative Review paper type, ‘Perspective’ papers are usually shorter than and more often give personal viewpoints or insights than do Narrative Reviews. On the other hand, Narrative Reviews take a comprehensive and critical survey of previous literature in the topic area. All ‘Perspective’ papers are peer reviewed. We recommend that the ‘Perspective’ manuscripts should contain the following items: <br>

    <b>ABSTRACT (Unstructured abstract)</b> <br>
    Should include the rationale for the ‘Perspective’; the aim of the ‘Perspective’; summary of the viewpoints shared in the ‘Perspective’; and main conclusions and their implications. <br>
    <b>Maximum length:</b> 250 words. <br> <br>

    <b>MAIN TEXT</b> <br>
    <b>Introduction:</b> What is the context, rationale, and aims of the ‘Perspectives’. <br>
    <b>Sub-sections relevant to the topic being discussed</b> 
    <b>Discussion:</b> Potential implications of the ‘Perspectives’. <br>
    <b>Maximum length:</b> 3,000 words (excluding abstract, tables, figures, acknowledgments, references) <span id="opinion"></span><br>
    <b>Keywords:</b> up to 10, listed in alphabetical order. <br>
    <b>Tables/Figures:</b> up to 3 tables and/or figures.
    
</p>

<h4>Opinion:</h4>
<p>
    The ASFIRJ Opinion papers are short articles in which authors, in a reflective format, present their personal insights and viewpoints on recent discoveries, subjects, techniques in the scientific community in a topic area, issues in society. While the structure of the Opinion paper is similar to a ‘Perspective’ paper, Opinion papers are usually shorter and focused on presentation of personal viewpoints or insights rather than a reliance on previous works in the topic area. Opinion papers do not have abstracts. Authors should use sub-headings that make their presentation logical and convey the content of their message. <span id="lc"></span>Opinion papers have a maximum word count of 1,500 words and may contain one table and/or figure. All Opinion manuscripts are peer reviewed.
    
    </p>

<h4 >Career Corner:</h4>
<p>
    ASFI’s core mandate is capacity building - supporting African scientists in developing and sustaining their research competencies. To complement ASFI’s ongoing capacity building programs, a section of ASFIRJ is dedicated to publishing articles that have research capacity building as a focus. Such articles will be educational in scope, providing resources in their content that enable interested researchers to increase their experiences and capacities. The Career Corner section of ASFIRJ will include articles on concepts, methodology, and tools used in specific topics or research areas. They should have abstracts of maximum 300 words. Authors should use sub-headings that make their presentation logical and convey the content of their message. <span id="fs"></span> Articles should have a maximum word count of 4,500 words and may contain up to 8 table and/or figure. All manuscripts submitted to the Career Corner are peer reviewed.
</p>

<h4 >Field Stories:</h4>
<p>
The scientific and research journey are full of various experiences that are encountered by scientists – whether in the wet lab, in society, in social experiments, or in clinical theatres. Some of these experiences may be novel, surprising, exciting, strange, or even weird. Such experiences can be individual or collective. Often these experiences are not documented as part of the publishing of scientific experiments and findings. We believe that sharing such experiences to the scientific community can support active interactions, experience sharing, and learning among scientists. ASFIRJ is therefore dedicating the Field Stories section of the journal to allow authors tell the story of their experiences in the field. We would like to read the stories and what makes them novel/weird/strange experiences. Papers submitted as Field Stories do not have abstracts. Authors should use sub-headings that make their presentation logical and convey the content of their message. <span id="proof"></span> The papers have a maximum word count of 1,000 words and may contain one table and/or figure. Papers submitted as Field Stories are not peer reviewed.</p>


<h4 >Proofs:</h4>
<p>
After acceptance, all manuscripts undergo editorial check and proofreading to ensure that the journal standards and formatting requirements are met. The Production Office works to ensure that all articles are published on time and accurately. Once the proof is ready, the corresponding author will receive an e-mail with the manuscript proof. The author-corrected proof must be returned within 48 hours of receipt of the email. The corresponding author should use the proof only to check the typesetting, editing, and correctness of the text, tables and figures. Substantial changes to the article as accepted for publication can only be done with the permission of the editor. Proofreading is the responsibility of the authors.<span id="appeal"></span> Therefore, corresponding authors should pay careful attention when checking the proofs before returning it to the journal, as the journal cannot guarantee any other subsequent corrections.</p>

<h4>Appeal of Manuscript Decisions: </h4>
<p>Authors have the right to appeal all decisions of the journal. All appeals will be reviewed by the editors and their decisions on such appeals are final. Authors appealing their decisions should submit a written appeal to the editorial office, providing detailed reasons for the appeal, plus point-by-point responses to the comments provided by the reviewers and/or editors. If the appeal is accepted, it does not mean that the paper is accepted, rather the paper will be reviewed again, either by the editorial board or sent back to the original or new peer reviewers.</p>

