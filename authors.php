                    <h2>Author Guidelines</h2>
                    <h4>Author Guidelines for ASFI Research Journal<span id="ob"></span></h4><br>
                    
                    <h4>OBJECTIVE:</h4>
<p>
ASFI Research Journal is committed to publishing high-quality, original research and other types of research and scholarly outputs that contribute to the advancement of knowledge and society within the African context and globally. <span id="sp"></span> Authors are encouraged to submit manuscripts that adhere to the journal's standards of excellence, promoting scholarly discourse and contributing to the diversity of academic disciplines.</p>

<h4>SUBMISSION PROCESS:</h4>
<p>Any manuscript being submitted to <i>ASFIRJ</i> must not have been previously published or submitted for publication elsewhere while under consideration in ASFIRJ except for abstracts in the proceedings of ASFI annual conference or other scientific meetings or symposia. Before initiating the submission process, authors should ensure that the manuscript and all accompanying materials have been prepared according to the specifications in the Author Guidelines. <span id="sr"></span> Once all materials are ready, manuscripts should be submitted online at: <a href="https://asfirj.org/submission" style="color: blueviolet;">www.asfirj.org/submission</a>. 
    </p>


<h4>Suggestion of Reviewers</h4>

<p>

During manuscript submission, authors are requested to submit the names and email addresses of potential reviewers. Reviewers who are colleagues, collaborators, or previous co-authors during the previous three years must not be suggested as this will constitute a potential conflict of interest. Authors should suggest reviewers from diverse expertise and attributes to ensure rigorous and balanced appraisal of their work. Suggested potential reviewers should exclude members of <i>ASFIRJ's</i> editorial team. <span id="rm"></span> Editors reserve the right whether or not to invite the reviewers suggested by the authors. </p>

<h4>Revision of Manuscripts </h4>
<p>Revised manuscripts must be submitted via ASFIRJ submission portal (www.asfirj.org/submission). Authors must ensure that the revised manuscript adheres to ASFIRJ formats and requirements. The revised manuscript must include the following items: <br>

    <b>(1) Responses to Reviewers’ Comments:</b> This must include point-by-point responses to the comments and concerns raised by the reviewers, editors, and editorial office. The authors’ responses should clearly specify how they have addressed the concerns raised, including clarity on any changes they made on the manuscript, tables, figures, and other parts of the manuscript. <br>
    
    <b>(2) Marked Revised Manuscript:</b> 

The revised main text of the manuscript should be done using the Microsoft Word’s Track Changes feature, which should show every alteration the authors made to the manuscript. If the authors are unable to use the Microsoft Word's Track Changes feature, they should underline the texts that have been added to the manuscript and then strikethrough the texts that have been deleted. <span id="dp"></span>Changes made to the tables and figures should also be indicated. Authors should use continuous line numbering throughout the Marked Manuscript.</p>



    <h4>Data Protection</h4>
    <p>
    
    The names, email addresses, affiliations, and other contact details <i>ASFIRJ</i> might require from submitting authors or reviewers will be used for the usual operations of the journal when necessary, sharing with relevant partners for the purpose of production and publication. <i>ASFIRJ</i> takes responsibility to protect all personal information and contact details collected from all users in the journal’s operation<span id="pp"></span> and ensures that necessary steps are taken to maintain the security, integrity, and privacy of all personal data collected and processed.</p>
    
    <h4>Preprint policy</h4>
    <p> 
    
    <i>ASFIRJ</i> will consider for review and, if positively reviewed, accepts manuscripts previously published on preprint servers. Authors are also free to post the submitted version of a manuscript to a preprint server at any time during the review process. If and when the article is accepted and published in ASFIRJ, <span id="use_of_ai"></span>authors should update any pre-publication versions in the preprint servers with a link to the final published articles on <i>ASFIRJ</i> website.
</p>

<h4>Use of AI in writing papers published in ASFIRJ</h4>
<p>Authors are responsible for ensuring the contents of their papers are correct and described according to international standards. Authors must comply with good research and writing practices, ensuring that plagiarism, falsification, or fabrication of content must not occur.</p>

<p>ASFIRJ recognizes the rapid increase in the use of AI-based tools (such as large language models, chatbots, or image creators) in scientific writing and we acknowledge that these tools may continuously remain accessible to researchers and authors. ASFIRJ does not prohibit the use of generative AI or other tools (digital or of another type) when writing the scientific paper. </p>

<p>However, we advise all authors to use the tools responsibly, ethically, and in ways that promote and accelerate scientific writing, and the science communication processes, while at the same time deterring any form of cheating.</p>

<p>Where such tools have been used in drawing up the manuscript, we ask that the authors disclose this in their manuscript. We ask that authors should clearly state in both the cover letter and the manuscript a description of the tools they have used and what was produced. In the manuscript, the disclosure should be included immediately after the “Acknowledgements” section and should have the following sub-title: “AI disclosure”. Such a declaration will promote transparency in scientific communication, facilitating compliance with the use of these emerging tools and technologies. <span id="mo"></span>It will also help to entrench trust within the scientific community, particularly between authors, readers, reviewers, and editors.</p>

