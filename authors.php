   <h4>Publication Ethics and Malpractice Statement</h4> 
   <h5>Introduction</h5>

   <p>ASFI Research Journal (ASFIRJ) is committed to maintaining the highest standards of publication ethics, editorial integrity, and research transparency. Articles are considered for publication solely on the basis of their scientific merit and quality. ASFIRJ expects authors, reviewers, editors, and editorial staff to conduct themselves in accordance with recognized international standards in scholarly publishing. This policy is aligned with the principles of the Committee on Publication Ethics (COPE) and informed by the ICMJE Recommendations, as well as reporting and transparency frameworks referenced by ASFIRJ, including CONSORT, STROBE, PRISMA, EQUATOR, TOP, and ARRIVE, where applicable. ASFIRJ will investigate suspected ethical breaches fairly, confidentially, and consistently, and may take editorial action including manuscript rejection, publication of corrections, expressions of concern, retractions, or referral to authors’ institutions where warranted, consistent with COPE guidance.</p>

   <h5>Authorship Criteria</h5>
   <p>ASFIRJ requires that all listed authors meet accepted authorship standards. In line with the ICMJE Recommendations, authorship should be based on all of the following:</p>
   <ul>
       <li>Substantial contribution to the conception or design of the work, or to the acquisition, analysis, or interpretation of data</li>
       <li>Drafting the manuscript or revising it critically for important intellectual content</li>
       <li>Final approval of the version to be published</li>
       <li>Agreement to be accountable for all aspects of the work</li>
   </ul>
   <p>Contributors who do not meet all authorship criteria should not be listed as authors but may be acknowledged with their permission. Activities such as funding acquisition alone, general supervision, administrative support, language editing, or proofreading do not by themselves justify authorship. Ghost authorship, guest authorship, honorary authorship, and undisclosed writing assistance are not permitted.</p>
   <p>The corresponding author is responsible for ensuring that the author list is accurate, that all authors approve the submitted and final versions, and that all authors have seen and agreed to any major revision, correction, or post-publication notice. Authorship changes after submission will require written agreement from all listed authors and, where necessary, may be referred back to the relevant institution(s) in line with COPE practice.</p>

   <h5>Statement of Originality</h5>
   <p>Submitted manuscripts must be original and must not have been previously published in substantially the same form or be under consideration by another journal at the time of submission. Authors must ensure that the work is their own, that all sources are properly cited, and that any use of previously published text, figures, tables, data, or ideas is appropriately attributed and, where needed, used with permission. This expectation supports ASFIRJ's emphasis on quality and scientific merit. Any related manuscripts, prior dissemination, preprints, conference abstracts, overlapping datasets, or secondary analyses must be disclosed to the editors at submission. Failure to disclose substantial overlap may be treated as redundant or duplicate publication and handled under the journal's misconduct procedures, consistent with ICMJE and COPE principles.</p>

   <h5>Ethics Approval Statement</h5>
   <p>Research involving human participants, human tissue, personal data, animals, or other ethically sensitive materials must have been conducted in accordance with applicable institutional, national, and international ethical requirements. Manuscripts reporting such work must include a clear ethics statement identifying the reviewing body, approval number or reference where available, and confirmation that informed consent was obtained when required. ASFIRJ's reviewer guidance also directs reviewers to assess ethical issues and adherence to relevant reporting standards. Where ethics approval was waived or deemed unnecessary, authors must state this explicitly and provide the basis for that determination. For clinical and health-related studies, authors should also follow applicable reporting and participant-protection standards referenced in the ICMJE Recommendations and the EQUATOR-aligned guidance cited by ASFIRJ.</p>

   <h5>Conflict of Interest Disclosures</h5>
   <p>All authors must disclose any financial and non-financial interests, relationships, activities, or affiliations that could reasonably be perceived to influence the work, its interpretation, or its presentation. This includes, but is not limited to, employment, consultancy, grants, advisory roles, equity interests, patents, honoraria, paid expert testimony, personal relationships, and institutional commitments. COI disclosure is a core element of the ICMJE Recommendations. Reviewers and editors must also disclose competing interests and recuse themselves from handling or reviewing manuscripts where a conflict could compromise, or reasonably appear to compromise, objectivity. When no competing interests exist, manuscripts should include a statement to that effect.</p>

   <h5>Data Integrity Standards</h5>
   <p>Authors are responsible for the accuracy, completeness, and integrity of all data, analyses, figures, tables, and conclusions presented in their manuscripts. Data fabrication, data falsification, inappropriate manipulation of images or results, selective reporting, and deliberate omission of relevant findings constitute serious misconduct and are prohibited. ASFIRJ's reviewer guidance already directs reviewers to examine figures, tables, data, and the correctness and reliability of the work. Authors must retain original data and supporting records for a reasonable period after publication and must provide them to the journal upon request where questions arise regarding the accuracy or integrity of the publication. The corresponding author should be prepared to respond to editorial queries before and after publication, consistent with ICMJE guidance. If concerns about data reliability arise, ASFIRJ may request raw data, seek clarification from authors, consult independent experts, or contact authors' institutions. Editorial responses may include rejection, correction, expression of concern, or retraction, following COPE guidance.</p>

   <h5>Data Sharing Policies</h5>
   <p>ASFIRJ encourages transparency, reproducibility, and responsible data sharing. Where ethically and legally possible, authors should make the data, code, protocols, and materials supporting their findings available in a trusted repository or through a reasonable access mechanism. This approach is consistent with the openness and transparency principles reflected in the TOP framework. Any restrictions to sharing or disclosing data must be explained clearly. Where datasets cannot be shared, authors should provide sufficient methodological detail to allow readers to understand and evaluate the study.</p>

   <h5>Plagiarism Policies</h5>
   <p>ASFIRJ does not tolerate plagiarism in any form. This includes direct copying without attribution, close paraphrasing without acknowledgment, mosaic plagiarism, translation plagiarism, misappropriation of ideas, and undisclosed text or figure reuse. Self-plagiarism and redundant publication are also unacceptable where substantial overlap is not disclosed and justified. These expectations are consistent with COPE and ICMJE guidance on originality and publication ethics. Submitted manuscripts may be screened for similarity, and concerns may also be raised during peer review or after publication. Where plagiarism or redundant publication is suspected, ASFIRJ will assess the extent and nature of overlap and may request author explanations, reject the manuscript, notify institutions, or publish a correction or retraction as appropriate. ASFIRJ's reviewer guidance specifically instructs reviewers to alert editors to suspected plagiarism or other unethical behavior.</p>

   <h5>AI Use Guidelines</h5>
   <p>Authors are responsible for ensuring the contents of their papers are correct and described according to international standards. Authors must comply with good research and writing practices, ensuring that plagiarism, falsification, or fabrication of content must not occur.</p>
   <p>ASFIRJ recognizes the rapid increase in the use of AI-based tools (such as large language models, chatbots, or image creators) in scientific writing and we acknowledge that these tools may continuously remain accessible to researchers and authors. ASFIRJ does not prohibit the use of generative AI or other tools (digital or of another type) when writing scientific papers.</p>
   <p>However, we advise all authors to use the tools responsibly, ethically, and in ways that promote and accelerate scientific writing, and the science communication processes, while at the same time deterring any form of cheating. Authors who use AI or AI-assisted tools for language editing, formatting, summarization, transcription, coding assistance, image preparation, or analytical support must disclose that use in the manuscript, including the nature of the tool's contribution. Authors remain fully responsible for verifying all outputs, citations, interpretations, calculations, and factual statements. AI-generated or AI-modified content that affects data, images, results, or conclusions must be clearly described, and its use must not mislead editors, reviewers, or readers. AI tools cannot be listed as authors, because authorship requires accountability, approval of the final version, and responsibility for the integrity of the work.</p>
   <p>Where such tools have been used in drawing up the manuscript, we ask that the authors disclose this in their manuscript. We ask that authors should clearly state in both the cover letter and the manuscript a description of the tools they have used and what was produced. In the manuscript, the disclosure should be included immediately after the "Acknowledgements" section and should have the following sub-title: "AI disclosure". Such a declaration will promote transparency in scientific communication, facilitating compliance with the use of these emerging tools and technologies. It will also help to entrench trust within the scientific community, particularly between authors, readers, reviewers, and editors.</p>
   <p>ASFIRJ's existing reviewer policy states that the journal does not accept the use of AI or AI-assisted tools in peer review or in generating peer review reports, because this may breach confidentiality and because reviewers remain solely responsible for their assessments. That reviewer prohibition forms part of this ethics statement and applies to all peer review conducted for the journal.</p>

   <h5>Journal Correction Policies</h5>
   <p>ASFIRJ is committed to preserving the integrity of the scholarly record. When errors or concerns are identified, the journal will act in a timely, transparent, and proportionate manner. Depending on the seriousness and nature of the issue, the journal may publish a correction, expression of concern, or retraction. COPE recommends that post-publication notices be clear, linked to the original article, and highly visible to readers.</p>
   <p>A correction may be issued where a publication remains reliable overall but contains an error that affects clarity, attribution, metadata, or a limited part of the scientific record. An expression of concern may be issued when there are serious unresolved concerns and an investigation is ongoing or inconclusive. A retraction may be issued when findings are unreliable due to major error or misconduct, when there has been plagiarism or duplicate publication, or when research was conducted or reported unethically.</p>
   <p>Retractions and other notices will identify the reason for the action and will remain permanently linked to the article record. Retraction is an editorial decision, supported by the publisher, and does not necessarily imply misconduct unless that has been established. Where needed, ASFIRJ may contact authors' institutions or other relevant bodies during investigations, consistent with COPE guidance on cooperation between journals and institutions.</p>

   <h5>Editorial and Reviewer Responsibilities</h5>
   <p>Editors will make decisions fairly, independently, and on the basis of scholarly merit, journal scope, ethical compliance, and the strength of peer review. ASFIRJ states that articles are accepted solely on the basis of scientific merit and quality, and the journal's reviewer guidance emphasizes constructive, neutral, and respectful review.</p>
   <p>Reviewers must treat manuscripts as confidential documents, declare conflicts of interest, provide objective and evidence-based feedback, and alert editors to ethical concerns including plagiarism, fraud, or reporting deficiencies. ASFIRJ's reviewer guidance expressly prohibits derogatory comments and prohibits the use of AI in the peer review process.</p>

   <h5>Misconduct Allegations and Investigations</h5>
   <p>ASFIRJ will take all credible allegations of research or publication misconduct seriously, whether they arise before or after publication. Concerns may include plagiarism, authorship disputes, undisclosed conflicts of interest, unethical research, image manipulation, data fabrication or falsification, duplicate publication, paper mill activity, or inappropriate AI use. COPE identifies handling allegations of misconduct as a core journal responsibility.</p>
   <p>Investigations will be handled confidentially and fairly. ASFIRJ may request explanations and documentary evidence from authors, consult reviewers or independent experts, and contact relevant institutions, funders, or ethics committees where appropriate. During an investigation, editorial consideration may be paused. After review, ASFIRJ may decide that no action is needed or may issue rejection, correction, expression of concern, retraction, or other editorial notices as warranted.</p>
